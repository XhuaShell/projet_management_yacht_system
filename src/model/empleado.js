export class Empleado{
    constructor(
        codigo,
        cedula,
        nombre,
        direccion,
        fecha_vinculacion,
        salario,
        telefono
    ){
        this.codigo=codigo;
        this.cedula=cedula;
        this.nombre=nombre;
        this.direccion=direccion;
        this.fecha_vinculacion=fecha_vinculacion;
        this.salario=salario;
        this.telefono=telefono;

    }
}